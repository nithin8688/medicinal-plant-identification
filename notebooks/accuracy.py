#Function to create base model

def create_base_model(model_class) :

  base_model=model_class(weights='imagenet', include_top=False, input_shape=(IMG_HEIGHT, IMG_WIDTH, 3))

  # Unfreeze top 20 layers for fine-tuning

  for layer in base_model.layers[-20:]:

    layer.trainable = True

  x = GlobalAveragePooling2D()(base_model.output)

  x=Dense (512, activation='relu')(x)

  x=Dropout (0.3)(x)

  predictions=Dense(train_generator.num_classes, activation="softmax") (x)

  model=Model(inputs=base_model.input, outputs=predictions)

  model.compile(optimizer=Adam(learning_rate=0.0001), loss='categorical_crossentropy', metrics=['accuracy'])

  return model

models = [create_base_model(InceptionV3), create_base_model(ConvNeXtTiny)]

