import tensorflow as tf
from tensorflow.keras.layers import Dense, Dropout, Input, BatchNormalization, Concatenate
from tensorflow.keras.models import Model
from tensorflow.keras.optimizers import AdamW
from tensorflow.keras.callbacks import ReduceLROnPlateau, EarlyStopping

def create_stacked_model(models):
    inputs = Input(shape=(IMG_HEIGHT, IMG_WIDTH, 3))

    features1 = models[0](inputs, training=False)
    features2 = models[1](inputs, training=False)

    merged_features = Concatenate()([features1, features2])

    # Fully Connected Layers
    x = Dense(384, activation='relu')(merged_features)
    x = BatchNormalization()(x)
    x = Dropout(0.15)(x)  # Increased Dropout for better generalization
    x = Dense(train_generator.num_classes, activation='softmax')(x)

    # ✅ Cosine Decay with Adjusted Parameters
    lr_schedule = tf.keras.optimizers.schedules.CosineDecayRestarts(
        initial_learning_rate=0.0002,
        first_decay_steps=8 * len(train_generator),
        t_mul=2.0,
        m_mul=0.8,
        alpha=0.0005  # Prevents LR from shrinking too fast
    )

    model = Model(inputs=inputs, outputs=x)
    model.compile(
        optimizer=AdamW(learning_rate=lr_schedule, weight_decay=1e-4),
        loss=tf.keras.losses.CategoricalCrossentropy(label_smoothing=0.2),  # Increased label smoothing
        metrics=['accuracy']
    )

    return model

stacked_model = create_stacked_model(models)

stacked_model.fit(
    train_generator,
    validation_data=validation_generator,
    epochs=25,
    batch_size=64,
    callbacks=[
        ReduceLROnPlateau(monitor='val_loss', factor=0.5, patience=3),
        EarlyStopping(monitor='val_accuracy', patience=8, restore_best_weights=True)
    ],
    verbose=1
)

stacked_accuracy = stacked_model.evaluate(validation_generator)[1]
print(f'Ensemble Model Accuracy: {stacked_accuracy * 100:.2f}%')

def predict_image(image_path, model):
    img = tf.keras.preprocessing.image.load_img(image_path, target_size=(IMG_HEIGHT, IMG_WIDTH))
    img_array = tf.keras.preprocessing.image.img_to_array(img)
    img_array = np.expand_dims(img_array, axis=0) / 255.0

    predictions = model.predict(img_array)
    predicted_class = np.argmax(predictions, axis=1)

    class_indices = {v: k for k, v in train_generator.class_indices.items()}
    predicted_label = class_indices[predicted_class[0]]

    return predicted_label

